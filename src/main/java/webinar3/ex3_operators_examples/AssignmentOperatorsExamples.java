package webinar3.ex3_operators_examples;

public class AssignmentOperatorsExamples {
    public static void main(String[] args) {
        int i = 1, j = 2, k = 0;

        k = i + j;

        i += 5; // i = i + 5;

        System.out.println(i);

        i -= 5;
        System.out.println(i);

        i *= 5;
        System.out.println(i);

        i /= 5;
        System.out.println(i);


        /*
        do sprawdzenia:
            -=
            *=
            /=
         */
    }
}

