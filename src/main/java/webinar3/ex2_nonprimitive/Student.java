package webinar3.ex2_nonprimitive;

public class Student {
    String name;
    int age;

    // gettery i settery
    // settery - ustawiają wartość zmiennej
    // gettery - zwracają wartość zmiennej

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }




}


